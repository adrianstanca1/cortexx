/**
 * server/workers/autoimprove-scheduler.js
 * Scheduled job that runs trend analysis and creates recommendations.
 */
const pool = require("../db");
const { analyzeAndRecommend } = require("../lib/autoimprove-analyser");
const { createNotification } = require("../lib/websocket");

/**
 * Run autoimprove analysis for a single tenant schedule.
 */
async function runAnalysis(schedule) {
  const {
    id: scheduleId,
    organization_id: orgId,
    company_id: companyId,
    budget_threshold: budgetThreshold,
    safety_threshold: safetyThreshold,
    defect_threshold: defectThreshold,
  } = schedule;

  try {
    const result = await analyzeAndRecommend(orgId, companyId, {
      budget_threshold: budgetThreshold,
      safety_threshold: safetyThreshold,
      defect_threshold: defectThreshold,
    });

    if (
      result.recommendations.length === 0 &&
      result.atRiskItems.length === 0
    ) {
      // Nothing significant found — just update timestamps
      await pool.query(
        `
        UPDATE autoimprove_schedules
        SET last_run_at = NOW(), next_run_at = NOW() + (frequency_hours || ' hours')::INTERVAL
        WHERE id = $1
      `,
        [scheduleId],
      );
      return;
    }

    // Store recommendations
    const client = await pool.connect();
    try {
      await client.query("BEGIN");

      for (const rec of result.recommendations) {
        await client.query(
          `
          INSERT INTO autoimprove_recommendations
            (organization_id, company_id, project_id, type, severity, recommendation, auto_actions)
          VALUES ($1, $2, $3, $4, $5, $6, $7)
        `,
          [
            orgId,
            companyId,
            rec.projectId,
            rec.type,
            rec.severity,
            rec.recommendation,
            JSON.stringify(rec.autoActions || []),
          ],
        );
      }

      await client.query(
        `
        UPDATE autoimprove_schedules
        SET last_run_at = NOW(), next_run_at = NOW() + (frequency_hours || ' hours')::INTERVAL
        WHERE id = $1
      `,
        [scheduleId],
      );

      await client.query("COMMIT");
    } catch (dbErr) {
      await client.query("ROLLBACK").catch((rbErr) => {
        console.error(
          "[autoimprove-scheduler] ROLLBACK failed:",
          rbErr.message,
        );
      });
      throw dbErr;
    } finally {
      client.release();
    }

    // Notify super_admin / company_owner for high-severity recommendations
    const highSeverity = result.recommendations.filter(
      (r) => r.severity === "high",
    );
    if (highSeverity.length > 0) {
      // Find admin users for this tenant
      const { rows: admins } = await pool.query(
        `
        SELECT id FROM users
        WHERE role IN ('super_admin', 'company_owner')
          AND (organization_id = $1 OR company_id = $2)
        LIMIT 5
      `,
        [orgId, companyId],
      );

      for (const admin of admins) {
        createNotification(
          admin.id,
          "Autoimprove Alert",
          `${highSeverity.length} high-severity recommendation(s) generated by Autoimprove`,
          highSeverity.length > 2 ? "error" : "warning",
          { scheduleId, recommendationCount: result.recommendations.length },
        );
      }
    }

    console.log(
      `[autoimprove-scheduler] Schedule ${scheduleId}: ${result.recommendations.length} recommendations, ${result.atRiskItems.length} at-risk items`,
    );
  } catch (err) {
    console.error(
      `[autoimprove-scheduler] Schedule ${scheduleId} failed:`,
      err.message,
    );

    // Retry logic: mark next_run_at + 1 hour on failure
    await pool
      .query(
        `
      UPDATE autoimprove_schedules
      SET next_run_at = NOW() + INTERVAL '1 hour'
      WHERE id = $1
    `,
        [scheduleId],
      )
      .catch((err) => {
        console.error(
          "[autoimprove-scheduler] Schedule update failed:",
          err.message,
        );
      });
  }
}

/**
 * Poll for due schedules and run analysis.
 */
async function pollSchedules() {
  let client = null;
  try {
    client = await pool.connect();
    await client.query("BEGIN");

    const res = await client.query(`
      SELECT id, organization_id, company_id, budget_threshold, safety_threshold, defect_threshold, frequency_hours
      FROM autoimprove_schedules
      WHERE enabled = true AND next_run_at <= NOW()
      ORDER BY next_run_at ASC
      LIMIT 1
      FOR UPDATE SKIP LOCKED
    `);

    if (res.rows.length === 0) {
      await client.query("COMMIT");
      return;
    }

    const schedule = res.rows[0];
    await client.query("COMMIT");
    client.release();

    await runAnalysis(schedule);
  } catch (err) {
    if (client)
      await client.query("ROLLBACK").catch((rbErr) => {
        console.error(
          "[autoimprove-scheduler] ROLLBACK failed:",
          rbErr.message,
        );
      });
    console.error("[autoimprove-scheduler] Polling error:", err.message);
  } finally {
    if (client) client.release();
    setTimeout(pollSchedules, 60000); // Poll every minute
  }
}

/**
 * Ensure a default schedule exists for a tenant on first run.
 * Called on first worker start.
 */
async function ensureDefaultSchedule(orgId, companyId) {
  const tid = orgId || companyId;
  if (!tid) return;

  const { rows } = await pool.query(
    `
    SELECT id FROM autoimprove_schedules
    WHERE COALESCE(organization_id, company_id) = $1
    LIMIT 1
  `,
    [tid],
  );

  if (rows.length === 0) {
    await pool.query(
      `
      INSERT INTO autoimprove_schedules (organization_id, company_id)
      VALUES ($1, $2)
    `,
      [orgId, companyId],
    );
    console.log(
      `[autoimprove-scheduler] Created default schedule for tenant ${tid}`,
    );
  }
}

function startAutoimproveScheduler() {
  console.log(
    "[autoimprove-scheduler] Started background polling for autoimprove schedules",
  );
  pollSchedules();
}

module.exports = { startAutoimproveScheduler };
